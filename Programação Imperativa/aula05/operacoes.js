let numero = 10;

console.log(`Adição : ${numero} + 100 = ${numero + 100}`);
console.log(`Subtração : ${numero} - 7 = ${numero - 7}`);
console.log(`Divisão : ${numero} / 5 = ${numero / 5}`);
console.log(`Multiplicação : ${numero} * 1000 = ${numero * 1000}`);
console.log(`Incremento - 1ª forma: ${numero} = ${++numero}`);
console.log(`Incremento - 2ª forma: ${numero++} = ${numero}`)
console.log(`Decremeto - 1ª forma: ${numero} = ${--numero}`);
console.log(`Decremento - 2ª forma: ${numero--} = ${numero}`);

